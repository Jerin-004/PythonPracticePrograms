## maths
import math

pi = 3.14

x = 1
y = 2
z = 3

print(round(pi))
print(math.ceil(pi))    ## takes next value 
print(math.floor(pi))   ## takes the previuos value
print(abs(pi))      ## turns a negative value to positive
print(pow(pi,2))    ## powers the value
print(math.sqrt(pi))  ## finds the sqrt root
print(max(x,y,z))    ## finds the maximum value
print(min(x,y,z))    ## finds the least value
